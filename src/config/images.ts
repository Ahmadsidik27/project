// Fungsi helper untuk mendapatkan gambar dengan fallback
export const getImage = (category: keyof typeof images, key: string, fallback?: string): string => {
  const categoryImages = images[category] as Record<string, string>;
  return categoryImages?.[key] || fallback || images.placeholders.course;
};

